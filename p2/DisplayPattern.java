public class DisplayPattern {
    public static void main(String[] args) {
        System.out.println("(\\__/)   I am an");
        System.out.println("( @W@)         OOO    W           W  L");
        System.out.println("/) W )       O     O    W   W   W    L");
        System.out.println(" ^   ^         OOO        W   W      L  L  L");
    }
    
}
